package com.day_3.esercizio2;

public class Esercizio2 {


	
	public void valore() {
	int n = 2;
	
	switch(n) {
	case 0:
		System.out.println("zero");
		break;
	case 1:
		System.out.println("uno");
		break;
	case 2:
		System.out.println("due");
		break;
	case 3:
		System.out.println("tre");
		break;
	default:
		System.out.println("Valore non valido");
		break;
	}
	}
	
}
